package task.loops;

public class seven {
   
    public static void main(String[] args) {
        System.out.println("Sequence of numbers:");

        for (int i = 7; i <= 98; i += 7) {
            System.out.print(i);

           
            if (i < 98) {
                System.out.print(", ");
            }
        }
    
}

}
